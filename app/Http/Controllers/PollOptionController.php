    {
        $pollOption = PollOption::where('poll_id', $id)->orderBy('id')->get();

        if ($pollOption->isEmpty()) {
            return response()->json([
                "status" => "error",
                "message" => "poll option not found"
            ]);
        }

        return response()->json([
            "status" => "success",
            "data" => $pollOption
        ]);
    }


